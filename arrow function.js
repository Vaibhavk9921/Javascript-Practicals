// Single Line Arrow Function

let add=(a,b)=>a+b
let area =(r)=>3.14*r*r
let z=add(100,200)
let c=area(10)
console.log(z)
console.log(c)

// Multi Line Arrow Function
let add1=(l,m)=>{
    let q=l+m
    return q
}
let area1=(y)=>{
    let w=3.14*y*y
    return w
}
let v=add1(100,200)
let f=area1(10)
console.log(v)
console.log(f)